current_weather = random.choice(WEATHERS)
current_date = dt.datetime.now(TZ_MOSCOW).date()


def update_weather_if_new_day():
    global current_weather, current_date
    now = dt.datetime.now(TZ_MOSCOW)
    if now.date() != current_date:
        current_date = now.date()
        current_weather = random.choice(WEATHERS)


current_minute = dt.datetime.now(TZ_MOSCOW).minute

def scheduler_loop():
    global current_weather, current_date
    while True:
        now = dt.datetime.now(TZ_MOSCOW)
        if now.date() != current_date:
            current_date = now.date()
            current_weather = random.choice(WEATHERS)
        time.sleep(30)


def send_message(vk, peer_id, text):
    vk.messages.send(
        peer_id=peer_id,
        random_id=get_random_id(),
        message=text
    )


def main():
    vk_session = vk_api.VkApi(token=GROUP_TOKEN)
    vk = vk_session.get_api()
    longpoll = VkBotLongPoll(vk_session, GROUP_ID)

    threading.Thread(target=scheduler_loop, daemon=True).start()

    print("Бот запущен")
    for event in longpoll.listen():
        if event.type == VkBotEventType.MESSAGE_NEW:
            msg = event.message  # актуальный способ получения dict сообщения[web:30]
            text = msg.get("text", "").strip()
            peer_id = msg["peer_id"]

            update_weather_if_new_day()

            if text.lower() == "!погода тест (цить куриная голова)":
                send_message(vk, peer_id, f"Текущая погода:\n\n{current_weather}")


if __name__ == "__main__":
    main()
